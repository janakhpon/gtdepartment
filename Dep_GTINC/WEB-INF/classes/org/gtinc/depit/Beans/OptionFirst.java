package org.gtinc.depit.Beans;

public class OptionFirst {

	

	private String stdName;
	private String stdEmail;
	private String stdAddress;
	private String stdBirthdate;
	private String stdGender;
	private String stdSubject;
	private String stdPhno;
	private String stdReg;
	private String stdDescription;

	

	public OptionFirst() {

	}

	public OptionFirst(String stdName, String stdEmail, String stdAddress, String stdBirthdate, String stdGender, String stdSubject, String stdPhno, String stdReg, String stdDescription) {
		this.stdName = stdName;
		this.stdEmail = stdEmail;
		this.stdAddress = stdAddress;
		this.stdBirthdate = stdBirthdate;
		this.stdGender = stdGender;
		this.stdSubject = stdSubject;
		this.stdPhno = stdPhno;
		this.stdReg = stdReg;
		this.stdDescription = stdDescription;
	}
	
	
	
	public String getStdName() {
		return stdName;
	}

	
	public void setStdName(String stdName) {
		this.stdName = stdName;
	}
	
	public String getStdEmail() {
		return stdEmail;
	}

	
	public void setStdEmail(String stdEmail) {
		this.stdEmail = stdEmail;
	}
	
	
	public String getStdAddress() {
		return stdAddress;
	}

	
	public void setStdAddress(String stdAddress) {
		this.stdAddress = stdAddress;
	}
	
	
	public String getStdBirthdate() {
		return stdBirthdate;
	}

	
	public void setStdBirthdate(String stdBirthdate) {
		this.stdBirthdate = stdBirthdate;
	}
	
	public String getStdGender() {
		return stdGender;
	}

	
	public void setStdGender(String stdGender) {
		this.stdGender = stdGender;
	}
	
	public String getStdSubject() {
		return stdSubject;
	}

	
	public void setStdSubject(String stdSubject) {
		this.stdSubject = stdSubject;
	}
	
	
	public String getStdPhno() {
		return stdPhno;
	}

	
	public void setStdPhno(String stdPhno) {
		this.stdPhno = stdPhno;
	}
	
	
	public String getStdReg() {
		return stdReg;
	}

	
	public void setStdReg(String stdReg) {
		this.stdReg = stdReg;
	}
	
	
	public String getStdDescription() {
		return stdDescription;
	}

	
	public void setStdDescription(String stdDescription) {
		this.stdDescription = stdDescription;
	}
	
	
}
